    /* Checks if external storage is available for read and write */
    public static boolean isExternalStorageWritable() {
        String state = Environment.getExternalStorageState();
        return Environment.MEDIA_MOUNTED.equals(state);
    }

    /* Checks if external storage is available to at least read */
    public static boolean isExternalStorageReadable() {
        String state = Environment.getExternalStorageState();
        return Environment.MEDIA_MOUNTED.equals(state) ||
                Environment.MEDIA_MOUNTED_READ_ONLY.equals(state);
    }

    public static void copyFile(final Context _context, final Uri srcUri, final File dst) throws IOException {
        InputStream in = _context.getContentResolver().openInputStream(srcUri);
        if (in == null) throw new IOException("Source file is not exists");
        OutputStream out = new FileOutputStream(dst);
        // Transfer bytes from in to out
        byte[] buf = new byte[1024];
        int len;
        while ((len = in.read(buf)) > 0) {
            out.write(buf, 0, len);
        }
        in.close();
        out.close();
    }

    public static void copyFile(final File src, final File dst) throws IOException {
        FileChannel inChannel = new FileInputStream(src).getChannel();
        FileChannel outChannel = new FileOutputStream(dst).getChannel();
        try {
            inChannel.transferTo(0, inChannel.size(), outChannel);
        } finally {
            if (inChannel != null) {
                inChannel.close();
            }
            if (outChannel != null) {
                outChannel.close();
            }
        }
    }

    public static File getExternalPrivatePath(final Context context, final String relativePath) {
        final File externalFilePath = context.getExternalFilesDir(null);
        if (externalFilePath == null) return null;
        String path = externalFilePath.getAbsolutePath();
        if (!TextUtils.isEmpty(relativePath)) path = path + "/" + relativePath;
        File file = new File(path);
        file.mkdirs();
        return file;
    }

    public static File generateFileNameInPrivatePath(final Context context, final String relativePath, final String fileNamePrefix) {
        final File path = getExternalPrivatePath(context, relativePath);
        if (path == null) return null;

        File dst;
        int i = 0;
        do {
            dst = new File(path.getAbsolutePath() + "/" + fileNamePrefix + i);
            i++;
        } while (dst.exists() || i > 150);

        if (i > 150) return null;

        return dst;
    }

    public static boolean clearDir(final String path) {
        boolean result = false;
        File dirToClear = new File(path);
        if (dirToClear.exists() && dirToClear.isDirectory()) {
            File[] files = dirToClear.listFiles();
            for (File file : files) {
                file.delete();
            }
            result = true;
        }
        return result;
    }

    public static void createDir(final String path) {
        File dir = new File(path);
        if (dir.exists()) return;
        dir.mkdir();
    }

    /**
     * Gets the extension of a file name, like ".png" or ".jpg".
     *
     * @param uri
     * @return Extension including the dot("."); "" if there is no extension;
     * null if uri was null.
     */
    public static String getExtension(String uri) {
        if (uri == null) {
            return null;
        }

        int dot = uri.lastIndexOf(".");
        if (dot >= 0) {
            return uri.substring(dot);
        } else {
            // No extension.
            return "";
        }
    }

    /**
     * @return Whether the URL is a local one.
     */
    public static boolean isLocal(String url) {
        if (url != null && !url.startsWith("http://") && !url.startsWith("https://")) {
            return true;
        }
        return false;
    }

    /**
     * @return True if Uri is a MediaStore Uri.
     */
    public static boolean isMediaUri(Uri uri) {
        return "media".equalsIgnoreCase(uri.getAuthority());
    }

    /**
     * Convert File into Uri.
     *
     * @param file
     * @return uri
     */
    public static Uri getUri(File file) {
        if (file != null) {
            return Uri.fromFile(file);
        }
        return null;
    }

    /**
     * Returns the path only (without file name).
     *
     * @param file
     * @return
     */
    public static File getPathWithoutFilename(File file) {
        if (file != null) {
            if (file.isDirectory()) {
                // no file to be split off. Return everything
                return file;
            } else {
                return file.getParentFile();
            }
        }
        return null;
    }

    /**
     * @return The MIME type for the given file.
     */
    public static String getMimeType(File file) {

        String extension = getExtension(file.getName());

        if (extension.length() > 0)
            return MimeTypeMap.getSingleton().getMimeTypeFromExtension(extension.substring(1));

        return "application/octet-stream";
    }

    /**
     * @return The MIME type for the give Uri.
     */
    public static String getMimeType(Context context, Uri uri) {
        File file = new File(getPath(context, uri));
        return getMimeType(file);
    }

